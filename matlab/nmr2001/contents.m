% GPNMR General purpose NMR analysis toolbox.
% A collection of programs for evaluation and fitting of Parma NMR data, especially designed 
% for solid-state NMR. Currently supported NMR hardware are TecMag-Apollo and Stelar 
% spectrometers (all versions).
%
% Version 1.0.2   20-Oct-2001
%
% User interface programs.
%   echoes      - Command-line or GUI based loading and weighted Fourier transform of 1-D and 
%                 2-D Apollo or Stelar data. 
%   tmgecho     - Wrapper of echoes: Tecmag data.
%   slrecho     - Wrapper of echoes: Stelar data.
%   peakfit     - Multi-peak fitting of spectral data via fminuit (gateway program). 
%   mgau        - Wrapper of peakfit: multi-gaussian.
%   mlore       - Wrapper of peakfit: multi-lorentzian.

